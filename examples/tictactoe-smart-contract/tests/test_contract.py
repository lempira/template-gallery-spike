
def test_addition():
    # Test basic addition
    result = 2 + 2
    assert result == 4

def test_addition_with_negatives():
    # Test addition with negative numbers
    result = -5 + 3
    assert result == -2

def test_addition_with_zero():
    # Test addition with zero
    result = 0 + 10
    assert result == 10 